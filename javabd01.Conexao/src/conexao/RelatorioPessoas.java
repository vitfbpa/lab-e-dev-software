    public void preencheTabela() {
        String sexo = "";
        PessoaDAO pDAO = new PessoaDAO();
        // List<Pessoa> listaPessoas = pDAO.getPessoas();
        
        if (rdoMasculino.isSelected())
            sexo = "M";
        else if (rdoFeminino.isSelected())
            sexo = "F";
        else
            sexo = "";
        
        List<Pessoa> listaPessoas = pDAO.getPessoasNome(txtNome.getText(), sexo);
        DefaultTableModel tabelaPessoas = (DefaultTableModel) tblRelatorio.getModel();
        
        tabelaPessoas.setNumRows(0);
        for(Pessoa p: listaPessoas) {
            Object[] obj = new Object[] {
                p.getId(),
                p.getNome(),
                p.getSexo(),
                p.getIdioma()};
                tabelaPessoas.addRow(obj);
            }
        }   
    
